import inspect
import logging
from datetime import datetime

import pytest

from bcd.config import Config

# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


# ------------------------------------------------------------------------------------------------ #
# pylint: disable=missing-class-docstring, line-too-long, no-member, logging-format-interpolation
# ------------------------------------------------------------------------------------------------ #
@pytest.mark.dbconfig
class TestDatabaseConfig:  # pragma: no cover
    # ============================================================================================ #
    def test_db_config(self):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        config = Config()
        assert config.get_name() == "bcd"
        assert config.get_mode() == "test"
        assert config.get_username() == "root"
        assert config.get_autocommit() is True
        assert config.get_timeout() == 30
        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\nCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)
