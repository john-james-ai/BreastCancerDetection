# ------------------------------------------------------------------------------------------------ #
# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.adapter
class TestAdapter:  # pragma: no cover
    # ============================================================================================ #
    def test_local(self):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        mode = "Stage"
        adapter = LocalAdapter(mode=mode)
        logger.info(adapter.tf_version)
        logger.info(adapter.wandb_api_key)
        logger.info(adapter.wandb_entity)
        logger.info(adapter.train_dir)
        logger.info(adapter.model_dir)
        # strategy = adapter.get_strategy()
        # assert isinstance(strategy, tf.distribute.Strategy)

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)

    # ============================================================================================ #
    def test_kaggle(self):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        mode = "Stage"
        adapter = KaggleAdapter(mode=mode)
        logger.info(adapter.tf_version)
        with pytest.raises(ModuleNotFoundError):
            logger.info(adapter.wandb_api_key)
            logger.info(adapter.wandb_entity)
            logger.info(adapter.train_dir)
        logger.info(adapter.model_dir)
        strategy = adapter.get_strategy()
        assert isinstance(strategy, tf.distribute.Strategy)
        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)
