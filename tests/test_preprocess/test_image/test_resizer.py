import inspect
import logging
from datetime import datetime

import pytest

from bcd.preprocess.image import Resizer

# ------------------------------------------------------------------------------------------------ #
# pylint: disable=missing-class-docstring, line-too-long
# ------------------------------------------------------------------------------------------------ #
# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.resize
class TestResizer:  # pragma: no cover
    # ============================================================================================ #
    def test_resizer(self, plt, caplog, images):
        caplog.set_level(logging.INFO)
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        r = Resizer()
        for i, image in enumerate(images):
            img_resized = r.run(image)
            plt.imshow(img_resized, cmap="turbo")
            filename = f"{plt.saveas[:-4]}_{str(i)}.pdf"
            plt.saveas = filename

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)

    # ============================================================================================ #
    def test_resizer_no_padding(self, plt, caplog, images):
        caplog.set_level(logging.INFO)
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        r = Resizer(keep_aspect=False)
        for i, image in enumerate(images):
            img_resized = r.run(image)
            plt.imshow(img_resized, cmap="turbo")
            filename = f"{plt.saveas[:-4]}_{str(i)}.pdf"
            plt.saveas = filename
        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)
